# Dictionary and sets 
# dictionary is a collection of key-value pairs 
# Syntax: keys   values
# a = {"keys" : "values"
# "Rajendra" : "coder"
# "marks" : "100"
# "list" : [7, 3, 4, 9]}
myDict = {
    "Rajendra" : "Coder", "marks" : "100", "list" : [7, 3, 4, 9]
}
print(myDict["Rajendra"])
print(myDict["marks"])
print(myDict["list"])